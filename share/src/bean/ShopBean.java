package bean;

public class ShopBean {

	private int shopid ;
	private String shopUserName ;
	private String shopUserPasswd ;
	private String shopName ;
	private String shopImg ;
	private String shopIntroduct;
	
	
	
	public String getShopIntroduct() {
		return shopIntroduct;
	}
	public void setShopIntroduct(String shopIntroduct) {
		this.shopIntroduct = shopIntroduct;
	}
	public int getShopid() {
		return shopid;
	}
	public void setShopid(int shopid) {
		this.shopid = shopid;
	}
	public String getShopUserName() {
		return shopUserName;
	}
	public void setShopUserName(String shopUserName) {
		this.shopUserName = shopUserName;
	}
	public String getShopUserPasswd() {
		return shopUserPasswd;
	}
	public void setShopUserPasswd(String shopUserPasswd) {
		this.shopUserPasswd = shopUserPasswd;
	}
	public String getShopName() {
		return shopName;
	}
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	public String getShopImg() {
		return shopImg;
	}
	public void setShopImg(String shopImg) {
		this.shopImg = shopImg;
	}
	
	
	
}
